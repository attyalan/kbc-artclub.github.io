$(document).ready(function(){
    $("#art").hover(function(){
        $("#art svg").css("fill", "#283583");
        $("#art p").css("color", "white");
        $("#art svg").css("width", "#110%");
        $("#art p").css("font-size", "2em");
        }, function(){
        $("#art svg").css("fill", "white");
        $("#art p").css("color", "#283583");
        $("#art svg").css("width", "#100%");
        $("#art p").css("font-size", "1.8em");
    });
});

